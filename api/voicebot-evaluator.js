// api/voicebot-evaluator.js
import { Router } from "express";
import OpenAI from "openai";
import "dotenv/config";

const router = Router();

const openai = new OpenAI({
  apiKey: process.env.OPENAI_API_KEY,
});

router.post("/api/voicebot-evaluator", async (req, res) => {
  try {
    const { input_text, evaluate_target } = req.body;

    // Validation
    if (!input_text || typeof input_text !== "string") {
      return res.status(400).json({
        success: false,
        error: "input_text is mandatory and must be a string",
      });
    }

    const systemPrompt = `
You are a **Voicebot Prompt Quality Assessor**.

You will be given a single response generated by a voicebot or assistant.

Evaluate the ${
      evaluate_target || "response"
    } based on the following 12 production-readiness metrics:
1. Context Understanding  
2. Hallucination Prevention  
3. Prompt Length  
4. Clarity  
5. Relevance  
6. Tone & Empathy  
7. Compliance with SOP/Policies  
8. Fallback Behavior  
9. Redundancy  
10. Grammar & Structure  
11. User Intent Understanding  
12. Output Format Consistency  

Return the result as a markdown table:

| # | Metric | Score (1-5) | Justification |
|---|---------|-------------|----------------|
| 1 | ... | ... | ... |

At the end, include:
**Total Points Scored**, **PASS %**, and **Status: PASS ✅ or FAIL ❌**
    `;

    const completion = await openai.chat.completions.create({
      model: "gpt-4o-mini",
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: input_text },
      ],
      temperature: 0.3,
    });

    const output_text = completion?.choices?.[0]?.message?.content?.trim();

    if (!output_text) {
      return res.status(500).json({
        success: false,
        error: "Model produced an empty response",
      });
    }

    return res.json({ success: true, output_text });
  } catch (err) {
    console.error("Voicebot evaluator error:", err);
    return res.status(500).json({
      success: false,
      error: "Internal Server Error",
      details: err.message,
    });
  }
});

export default router;
